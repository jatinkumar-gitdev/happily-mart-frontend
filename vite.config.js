import { defineConfig, loadEnv } from "vite";
import react from "@vitejs/plugin-react";

export default defineConfig(({ mode }) => {
  const env = loadEnv(mode, process.cwd(), "");

  return {
    plugins: [react()],
    build: {
      outDir: "dist"
    },
    server: {
      port: 3001,
      proxy: {
        "/api": {
          target: env.VITE_API_URL || "http://localhost:8800",
          changeOrigin: true,
        },
        "/uploads": {
          target: env.VITE_API_URL || "http://localhost:8800",
          changeOrigin: true,
        },
        "/avatars": {
          target: env.VITE_API_URL || "http://localhost:8800",
          changeOrigin: true,
        },
      },
    },
  };
});

